def fetch_series(client: EiaClient, aeo_year: int, series_name: str,
                 scenario_ids: list[str], region_ids: list[str],
                 value_col: str,
                 raw_output_path: Path | None = None,
                 start_year: int | None = None,
                 end_year: int | None = None) -> pd.DataFrame:
    """Fetch an AEO series for all scenarios/regions."""
    if (start_year is None) != (end_year is None):
        raise ValueError(
            "Both start_year and end_year must be provided together for fetch_series."
        )
    if start_year is not None and end_year is not None and start_year > end_year:
        raise ValueError(f"Invalid year window: start_year={start_year} > end_year={end_year}")

    series_ids = resolve_series_ids(client, aeo_year, series_name)

    params: list[tuple[str, str]] = [("data[]", "value")]
    if start_year is not None and end_year is not None:
        params.append(("start", str(start_year)))
        params.append(("end", str(end_year)))
    params.extend(("facets[scenario][]", s) for s in scenario_ids)
    params.extend(("facets[regionId][]", r) for r in region_ids)
    params.extend(("facets[seriesId][]", s) for s in series_ids)

    df = client.get_data(aeo_year, params)
    if raw_output_path is not None:
        raw_output_path.parent.mkdir(parents=True, exist_ok=True)
        df.to_csv(raw_output_path, index=False, float_format="%.6f")
        LOGGER.info("Wrote raw data for '%s' to %s (%d rows)",
                    series_name, raw_output_path, len(df))

    df = df.rename(columns={"scenario": "scenario_id"})
    df["region"] = df["regionName"].map(_region_label)
    df["year"] = pd.to_numeric(df["period"], errors="coerce").astype(int)
    df[value_col] = pd.to_numeric(df["value"], errors="coerce")
    df = df.dropna(subset=[value_col])
    df = df[df["scenario_id"].isin(scenario_ids)]

    out = (df.groupby(["scenario_id", "region", "year"], as_index=False)
             [value_col].mean())
    LOGGER.info("Fetched '%s': %d rows (%d scenarios x %d regions x %d years)",
                series_name, len(out), out["scenario_id"].nunique(),
                out["region"].nunique(), out["year"].nunique())
    return out


# ============================================================================
# Joint Beta Regression
# ============================================================================

def estimate_joint_betas(
    price: pd.DataFrame,
    demand: pd.DataFrame,
    deflator: float,
    regions: list[str],
) -> tuple[float, float, dict[str, float], dict[str, dict], pd.DataFrame, float]:
    """
    Estimate all betas jointly from the full equation:

        Price_2004(r,t,s) = Alpha(r,t) + Beta_reg(r)*Q_reg(r,t,s) + Beta_nat*Q_nat(t,s)

    Demeaning across scenarios within each (region, year) removes Alpha.
    Ordinary least squares (OLS) on the demeaned system estimates Beta_nat
    and all Beta_reg simultaneously.

    Returns: (beta_nat, beta_nat_r2, beta_reg_dict, regional_diagnostics,
              regression_data, model_r2)
    """
    # National demand = sum of regional demands per scenario-year
    demand_nat = (demand.groupby(["scenario_id", "year"], as_index=False)
                  ["demand"].sum()
                  .rename(columns={"demand": "demand_nat"}))

    merged = price.merge(demand, on=["scenario_id", "region", "year"])
    merged = merged.merge(demand_nat, on=["scenario_id", "year"])
    merged["price_2004"] = merged["price"] * deflator

    # Demean across scenarios within each (region, year) group
    for col, dcol in [("price_2004", "dp"), ("demand", "dq_reg"),
                      ("demand_nat", "dq_nat")]:
        means = merged.groupby(["year", "region"])[col].transform("mean")
        merged[dcol] = merged[col] - means

    # Build X matrix: [dq_nat, dq_reg(region1), dq_reg(region2), ...]
    region_arr = merged["region"].to_numpy()
    dq_reg_x = merged["dq_reg"].to_numpy()
    x_cols = [merged["dq_nat"].to_numpy()]
    for region in regions:
        x_cols.append(np.where(region_arr == region, dq_reg_x, 0.0))
    X = np.column_stack(x_cols)
    y = merged["dp"].to_numpy()

    # Ordinary least squares
    coeffs = np.linalg.lstsq(X, y, rcond=None)[0]
    beta_nat = float(coeffs[0])
    beta_reg = {region: float(coeffs[i + 1]) for i, region in enumerate(regions)}

    # Diagnostics
    y_hat = X @ coeffs
    model_r2 = _calc_r2(y, y_hat)

    # Partial R2 for national beta (residualize out regional terms)
    merged["dp_hat"] = y_hat
    merged["beta_reg_val"] = merged["region"].map(beta_reg)
    merged["dp_partial_nat"] = (
        merged["dp"] - merged["beta_reg_val"] * merged["dq_reg"]
    )
    beta_nat_r2 = _calc_r2(
        merged["dp_partial_nat"].to_numpy(),
        beta_nat * merged["dq_nat"].to_numpy())

    # Per-region partial R2 (residualize out national term)
    merged["dp_partial_reg"] = merged["dp"] - beta_nat * merged["dq_nat"]
    regional_diag: dict[str, dict] = {}
    for region in regions:
        reg = merged[merged["region"] == region]
        if reg.empty:
            continue
        regional_diag[region] = {
            "r2_partial": _calc_r2(
                reg["dp_partial_reg"].to_numpy(),
                beta_reg[region] * reg["dq_reg"].to_numpy()),
            "r2_full": _calc_r2(
                reg["dp"].to_numpy(), reg["dp_hat"].to_numpy()),
            "n_obs": len(reg),
        }

    # Log results
    LOGGER.info("Joint model: beta_nat=%.10f (partial R2=%.4f, model R2=%.4f)",
                beta_nat, beta_nat_r2, model_r2)
    for region in regions:
        diag = regional_diag.get(region, {})
        LOGGER.info("  %-25s beta=%.10f  (partial R2=%.4f, n=%d)",
                    region, beta_reg[region],
                    diag.get("r2_partial", float("nan")),
                    diag.get("n_obs", 0))

    return beta_nat, beta_nat_r2, beta_reg, regional_diag, merged, model_r2


