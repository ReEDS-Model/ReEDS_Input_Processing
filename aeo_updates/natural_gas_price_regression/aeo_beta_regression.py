def main() -> int:
    parser = argparse.ArgumentParser(
        description="Estimate NG beta coefficients from AEO scenarios")
    parser.add_argument("--config", default="aeo_pipeline_config.json")
    parser.add_argument("--log-level", default="INFO",
                        choices=["DEBUG", "INFO", "WARNING", "ERROR"])
    parser.add_argument("--aeo-year", type=int, default=None)
    args = parser.parse_args()

    setup_logging(args.log_level)

    script_dir = Path(__file__).resolve().parent
    cfg_path = resolve_config_path(args.config, script_dir)

    with cfg_path.open() as f:
        config = json.load(f)
    if args.aeo_year is not None:
        config["aeo_year"] = args.aeo_year

    run(config, script_dir)
    return 0


