def run(config: dict[str, Any], base_dir: Path) -> None:
    """Main pipeline: discover scenarios, fetch data from EIA API,
    run joint beta regression, and write all outputs.
    """
    ng_cfg = config["ng"]

    # Load shared region mapping from config
    global REGION_TO_LABEL
    REGION_TO_LABEL = ng_cfg["cendiv_and_label"]

    aeo_year = int(config["aeo_year"])
    deflator = float(ng_cfg["price_deflator_to_2004"])
    regions_config = list(ng_cfg["cendiv_and_label"].keys())
    regions = [REGION_TO_LABEL[r] for r in regions_config]

    # API key
    api_key = resolve_api_key(config)
    client = EiaClient(config["api"], api_key)

    scenarios_cfg = config.get("scenarios", {})
    if scenarios_cfg is None:
        scenarios_cfg = {}
    if not isinstance(scenarios_cfg, dict):
        raise ValueError("Config key 'scenarios' must be an object.")
    beta_scen_cfg = scenarios_cfg.get("beta_regression", {})
    if beta_scen_cfg is None:
        beta_scen_cfg = {}
    if not isinstance(beta_scen_cfg, dict):
        raise ValueError("Config key 'scenarios.beta_regression' must be an object.")

    include_cfg = beta_scen_cfg.get("include")
    if include_cfg is not None and not isinstance(include_cfg, list):
        raise ValueError("Config key 'scenarios.beta_regression.include' must be a list.")
    include_scenarios = [str(x).strip() for x in (include_cfg or []) if str(x).strip()]

    exclude_aliases = [str(x).strip() for x in beta_scen_cfg.get("exclude_aliases", [])]

    _sy = beta_scen_cfg.get("start_year")
    _ey = beta_scen_cfg.get("end_year")
    beta_start_year = int(_sy) if _sy is not None else None
    beta_end_year = int(_ey) if _ey is not None else None
    if (beta_start_year is None) != (beta_end_year is None):
        raise ValueError(
            "Config keys 'scenarios.beta_regression.start_year' and "
            "'scenarios.beta_regression.end_year' must be provided together."
        )
    if beta_start_year is not None and beta_end_year is not None and beta_start_year > beta_end_year:
        raise ValueError(
            "Config key 'scenarios.beta_regression': start_year cannot be greater than end_year."
        )
    if beta_start_year is not None and beta_end_year is not None:
        LOGGER.info(
            "Beta regression year range from config: %s-%s",
            beta_start_year, beta_end_year,
        )
    else:
        LOGGER.info("Beta regression year range: all available AEO years.")

    # Step 1: Discover scenarios
    LOGGER.info("Step 1: Discovering AEO %d scenarios...", aeo_year)
    scenario_ids = discover_regression_scenarios(
        client,
        aeo_year,
        include_scenarios=include_scenarios or None,
        exclude_aliases=exclude_aliases or None,
    )

    # Resolve region IDs
    region_ids = list(
        resolve_region_ids(client, aeo_year, regions_config).values()
    )

    # Save raw facets for reference
    out_dir = base_dir / "outputs of beta regression"
    raw_dir = out_dir / "raw_aeo_data"
    raw_dir.mkdir(parents=True, exist_ok=True)
    pd.DataFrame(client.get_facets(aeo_year, "scenario")).to_csv(
        raw_dir / "scenario_facets.csv", index=False)
    pd.DataFrame({"selected_scenario_id": scenario_ids}).to_csv(
        raw_dir / "selected_scenarios.csv", index=False)

    # Step 2: Fetch data
    LOGGER.info("Step 2: Fetching price and demand data...")
    price_raw = fetch_series(
        client, aeo_year, NG_SERIES_NAMES["price"],
        scenario_ids, region_ids, "price",
        raw_output_path=raw_dir / "raw_ng_price.csv",
        start_year=beta_start_year,
        end_year=beta_end_year,
    )
    demand_raw = fetch_series(
        client, aeo_year, NG_SERIES_NAMES["demand_elec"],
        scenario_ids, region_ids, "demand",
        raw_output_path=raw_dir / "raw_ng_demand_elec.csv",
        start_year=beta_start_year,
        end_year=beta_end_year,
    )
    if price_raw.empty or demand_raw.empty:
        raise ValueError(
            "No data available for configured beta regression year range."
        )
    LOGGER.info(
        "Price years used: %s-%s",
        int(price_raw["year"].min()),
        int(price_raw["year"].max()),
    )
    LOGGER.info(
        "Demand years used: %s-%s",
        int(demand_raw["year"].min()),
        int(demand_raw["year"].max()),
    )

    # Step 3: Estimate betas
    LOGGER.info("Step 3: Estimating betas (joint fixed-effects regression)...")
    (beta_nat, beta_nat_r2, beta_reg, regional_diag,
     reg_data, model_r2) = estimate_joint_betas(
        price_raw,
        demand_raw,
        deflator,
        regions,
    )

    # Step 4: Save outputs
    LOGGER.info("Step 4: Saving outputs...")
    save_outputs(out_dir, beta_nat, beta_nat_r2, beta_reg,
                 regional_diag, reg_data, model_r2,
                 first_model_year=int(config["start_year"]))

    # Print summary
    print("\n" + "=" * 60)
    print("RESULTS")
    print("=" * 60)
    print(f"\nNational beta: {beta_nat:.15f}")
    print(f"Model R2:      {model_r2:.6f}")
    print(f"\nRegional betas:")
    for region, beta in beta_reg.items():
        diag = regional_diag.get(region, {})
        print(f"  {region:25s}  {beta:.12f}  "
              f"(partial R2={diag.get('r2_partial', float('nan')):.4f})")
    print(f"\nOutputs: {out_dir}")
    print("\nNext commands:")
    print("  python sync_beta_to_alpha_inputs.py --config aeo_pipeline_config.json")
    print("  python aeo_alpha_regression.py --config aeo_pipeline_config.json")
    print("  python results_visualization.py --config aeo_pipeline_config.json")
