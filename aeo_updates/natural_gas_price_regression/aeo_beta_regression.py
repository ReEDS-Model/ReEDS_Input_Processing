def save_outputs(
    out_dir: Path,
    beta_nat: float,
    beta_nat_r2: float,
    beta_reg: dict[str, float],
    regional_diag: dict[str, dict],
    reg_data: pd.DataFrame,
    model_r2: float,
    first_model_year: int,
) -> None:
    """Write beta tables, regression diagnostics, and alpha candidates to CSV."""
    out_dir.mkdir(parents=True, exist_ok=True)

    # cd_beta0.csv
    pd.DataFrame({
        "*cendiv": list(beta_reg.keys()),
        "value": list(beta_reg.values()),
    }).to_csv(out_dir / "cd_beta0.csv", index=False, float_format="%.6f")

    # national_beta.csv
    pd.DataFrame([{
        "beta": beta_nat,
    }]).to_csv(out_dir / "national_beta.csv", index=False, float_format="%.6f")

    # Summary table
    rows = [{
        "scope": "national", "region": "ALL", "beta": beta_nat,
        "r2": beta_nat_r2, "r2_full": model_r2, "n_obs": len(reg_data),
    }]
    for region, beta in beta_reg.items():
        diag = regional_diag.get(region, {})
        rows.append({
            "scope": "regional", "region": region, "beta": beta,
            "r2": diag.get("r2_partial", float("nan")),
            "r2_full": diag.get("r2_full", float("nan")),
            "n_obs": diag.get("n_obs", 0),
        })
    pd.DataFrame(rows).to_csv(
        out_dir / "beta_regression_summary.csv",
        index=False, float_format="%.6f")

    # Regression point data for review
    point_cols = ["scenario_id", "year", "region", "demand", "demand_nat",
                  "price", "price_2004", "dp", "dq_reg", "dq_nat",
                  "dp_partial_reg", "dp_partial_nat", "dp_hat"]
    available = [c for c in point_cols if c in reg_data.columns]
    reg_data[available].to_csv(
        out_dir / "regression_points.csv", index=False, float_format="%.6f")

    # Alpha from beta-step data:
    # 1) Scenario residual alpha candidate:
    #    alpha_candidate(r,t,s) = price_2004 - beta_reg*regional_demand - beta_nat*national_demand
    #    except in the first modeled year, where ReEDS disables both beta terms
    #    and alpha must therefore equal the full converted price.
    # 2) Model alpha indexed by (region, year):
    #    alpha_shared(r,t) = mean_s(alpha_candidate(r,t,s))
    alpha_need = {"scenario_id", "year", "region", "price_2004", "demand", "demand_nat"}
    missing_alpha_cols = sorted(alpha_need - set(reg_data.columns))
    if missing_alpha_cols:
        raise ValueError(
            f"Regression data missing columns needed for alpha export: {missing_alpha_cols}"
        )

    alpha_long = reg_data[
        ["scenario_id", "year", "region", "price_2004", "demand", "demand_nat"]
    ].copy()
    alpha_long = alpha_long.rename(
        columns={"demand": "demand_elec_quads", "demand_nat": "q_nat"}
    )
    alpha_long["beta_reg"] = alpha_long["region"].map(beta_reg)
    if alpha_long["beta_reg"].isna().any():
        bad = sorted(alpha_long.loc[alpha_long["beta_reg"].isna(), "region"].astype(str).unique().tolist())
        raise ValueError(f"Missing regional beta values while exporting alpha: {bad}")
    alpha_long["beta_nat"] = beta_nat
    alpha_long["alpha_candidate"] = (
        alpha_long["price_2004"]
        - alpha_long["beta_reg"] * alpha_long["demand_elec_quads"]
        - alpha_long["beta_nat"] * alpha_long["q_nat"]
    )
    first_year_mask = alpha_long["year"] == first_model_year
    alpha_long.loc[first_year_mask, "alpha_candidate"] = alpha_long.loc[first_year_mask, "price_2004"]
    alpha_long = alpha_long.sort_values(["scenario_id", "year", "region"]).reset_index(drop=True)
    alpha_long.to_csv(
        out_dir / "alpha_from_beta_by_scenario.csv",
        index=False,
        float_format="%.6f",
    )

    alpha_shared = (
        alpha_long.groupby(["region", "year"], as_index=False)["alpha_candidate"]
        .mean()
        .rename(columns={"alpha_candidate": "alpha_2004"})
    )
    n_scenarios = (
        alpha_long.groupby(["region", "year"], as_index=False)["scenario_id"]
        .nunique()
        .rename(columns={"scenario_id": "n_scenarios_used"})
    )
    alpha_shared = alpha_shared.merge(n_scenarios, on=["region", "year"], how="left")
    alpha_shared = alpha_shared.sort_values(["region", "year"]).reset_index(drop=True)
    alpha_shared.to_csv(
        out_dir / "alpha_from_beta_regression.csv",
        index=False,
        float_format="%.6f",
    )

    alpha_wide_dir = out_dir / "alpha from beta regression"
    alpha_wide_dir.mkdir(parents=True, exist_ok=True)
    # Remove stale per-scenario files from earlier output format.
    for old in alpha_wide_dir.glob("alpha_from_beta_*.csv"):
        old.unlink()
    old_index = alpha_wide_dir / "alpha_from_beta_files.csv"
    if old_index.exists():
        old_index.unlink()

    alpha_wide = (
        alpha_shared.pivot_table(
            index="year",
            columns="region",
            values="alpha_2004",
            aggfunc="mean",
        )
        .sort_index()
        .reset_index()
    )
    alpha_wide.to_csv(
        alpha_wide_dir / "alpha_from_beta_shared.csv",
        index=False,
        float_format="%.6f",
    )

    LOGGER.info("Wrote outputs to %s", out_dir)


