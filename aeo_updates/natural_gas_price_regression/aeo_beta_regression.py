"""
NG Beta Regression: Estimate Regional and National Betas from AEO Scenarios
============================================================================

This script is a preprocessing step that runs BEFORE the main aeo_pipeline.py.
It estimates the beta coefficients used in the ReEDS NG supply curve model.

Method
------
The NG supply curve decomposes regional prices as:

    Price(r,t) = Alpha(r,t) + Beta_reg(r) * Q_reg(r,t) + Beta_nat * Q_nat(t)

Alpha varies by region and year (absorbing all time-varying effects).
Beta_reg and Beta_nat are structural parameters estimated via regression.

Estimation uses a joint fixed-effects regression across all regions:

    Price_2004(r,t,s) = Alpha(r,t) + Beta_reg(r) * Q_reg(r,t,s) + Beta_nat * Q_nat(t,s)

Alpha(r,t) is absorbed by demeaning each variable across scenarios within
each (region, year) group. The joint ordinary least squares (OLS) regression
then estimates Beta_nat and all regional Beta_reg values simultaneously.

Data: By default uses ALL available years for selected AEO scenarios
EXCEPT High/Low O&G Supply (held out for alpha computation), but
scenario and year selection can be overridden in config under
scenarios.beta_regression.

Usage
-----
    python aeo_beta_regression.py --config aeo_pipeline_config.json

Outputs
-------
    outputs of beta regression/cd_beta0.csv                  : Regional betas
    outputs of beta regression/national_beta.csv              : National beta
    outputs of beta regression/beta_regression_summary.csv    : Full summary
    outputs of beta regression/alpha_from_beta_regression.csv : Shared alpha(region, year)
    outputs of beta regression/alpha_from_beta_by_scenario.csv : Scenario residual alpha candidates
    outputs of beta regression/alpha from beta regression/alpha_from_beta_shared.csv : Wide shared alpha
    outputs of beta regression/*.png                          : Generated by results_visualization.py
"""

